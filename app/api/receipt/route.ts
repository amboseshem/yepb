export const runtime = "nodejs";

import { NextResponse } from "next/server";
import { prisma } from "@/lib/prisma";
import {
  PDFDocument,
  StandardFonts,
  rgb,
} from "pdf-lib";

export async function GET() {
  try {
    // 📊 DATA
    let totalMembers = 0;
    let totalContributions = 0;
    let totalProjects = 0;

    try {
      totalMembers = await prisma.memberProfile.count();
      totalContributions = await prisma.contribution.count();
      totalProjects = await prisma.project.count();
    } catch (e) {
      console.log("DB error:", e);
    }

    // 📄 PDF
    const pdfDoc = await PDFDocument.create();
    const page = pdfDoc.addPage([600, 800]);

    const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
    const bold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

    const { width, height } = page.getSize();

    // 🎨 HEADER BACKGROUND
    page.drawRectangle({
      x: 0,
      y: height - 100,
      width: width,
      height: 100,
      color: rgb(0.1, 0.35, 0.9),
    });

    // 🏷 TITLE
    page.drawText("YEP PLATFORM REPORT", {
      x: 180,
      y: height - 60,
      size: 18,
      font: bold,
      color: rgb(1, 1, 1),
    });

    // 📅 DATE
    page.drawText(new Date().toLocaleString(), {
      x: 50,
      y: height - 120,
      size: 10,
      font,
      color: rgb(0.4, 0.4, 0.4),
    });

    // 🔹 SECTION TITLE
    page.drawText("System Summary", {
      x: 50,
      y: height - 160,
      size: 14,
      font: bold,
    });

    // 📊 TABLE HEADER
    let startY = height - 190;

    page.drawRectangle({
      x: 50,
      y: startY,
      width: 500,
      height: 25,
      color: rgb(0.2, 0.6, 1),
    });

    page.drawText("Metric", {
      x: 60,
      y: startY + 7,
      size: 12,
      font: bold,
      color: rgb(1, 1, 1),
    });

    page.drawText("Value", {
      x: 400,
      y: startY + 7,
      size: 12,
      font: bold,
      color: rgb(1, 1, 1),
    });

    // 📋 TABLE ROWS
    const rows = [
      ["Total Members", totalMembers],
      ["Total Contributions", totalContributions],
      ["Total Projects", totalProjects],
    ];

    let y = startY - 25;

    rows.forEach((row, i) => {
      page.drawRectangle({
        x: 50,
        y: y,
        width: 500,
        height: 25,
        color: i % 2 === 0 ? rgb(0.95, 0.95, 0.95) : rgb(1, 1, 1),
      });

      page.drawText(String(row[0]), {
        x: 60,
        y: y + 7,
        size: 11,
        font,
      });

      page.drawText(String(row[1]), {
        x: 400,
        y: y + 7,
        size: 11,
        font,
      });

      y -= 25;
    });

    // 💰 FINANCIAL SECTION
    page.drawText("Financial Summary", {
      x: 50,
      y: y - 30,
      size: 14,
      font: bold,
    });

    page.drawText("Coming Soon (Linked to Wallet System)", {
      x: 50,
      y: y - 55,
      size: 10,
      font,
      color: rgb(0.5, 0.5, 0.5),
    });

    // ✍ SIGNATURE
    page.drawText("Authorized Signature:", {
      x: 50,
      y: 120,
      size: 10,
      font,
    });

    page.drawLine({
      start: { x: 180, y: 120 },
      end: { x: 350, y: 120 },
      thickness: 1,
    });

    // 📌 FOOTER
    page.drawText("Generated by YEP System", {
      x: 200,
      y: 40,
      size: 10,
      font,
      color: rgb(0.5, 0.5, 0.5),
    });

    const pdfBytes = await pdfDoc.save();
    const buffer = Buffer.from(pdfBytes);

    return new NextResponse(buffer, {
      headers: {
        "Content-Type": "application/pdf",
      },
    });
  } catch (error) {
    console.error(error);
    return NextResponse.json(
      { error: "Error generating PDF" },
      { status: 500 }
    );
  }
}